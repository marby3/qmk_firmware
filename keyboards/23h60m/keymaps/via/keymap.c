oled_rotation_t oled_init_user(oled_rotation_t rotation) {
    if (is_keyboard_master()){
    return OLED_ROTATION_270;
    }
    return rotation;  // flips the display 180 degrees if offhand
}

void render_layer_state(void) {
    oled_write_P(PSTR("Mode:\n"), false);
    oled_write_P(PSTR("MAIN\n"), get_highest_layer(layer_state) == _BASE);
    oled_write_P(PSTR("Lower\n"), layer_state_is(_LOWER));
    oled_write_P(PSTR("Raise\n"), layer_state_is(_RAISE));
    oled_write_P(PSTR("Adju\n"), layer_state_is(_ADJUST));
}

bool oled_task_user(void) {
    if (is_keyboard_master()) {
        render_layer_state();
    } else {
        render_logo();
        oled_scroll_right();  // Turns on scrolling
    }
    return false;
}
